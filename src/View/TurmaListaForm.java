    public void preencherTabela(){
                                contador=0;
                                DefaultTableModel tabela = (DefaultTableModel) tblTurma.getModel();
                                tblTurma.setModel(tabela);
                                tblTurma.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
                                tblTurma.getColumnModel().getColumn(0).setPreferredWidth(80);
                                tblTurma.getColumnModel().getColumn(1).setPreferredWidth(350);
                                tblTurma.getColumnModel().getColumn(2).setPreferredWidth(350);
                                tblTurma.getColumnModel().getColumn(3).setPreferredWidth(350);
                                 tabela.setNumRows(0);
                                 TurmaDao dao = new TurmaDao();

                                 for (TurmaController u : dao.CarregarTurmasTodas()) {
                                         tabela.addRow(new Object[]{u.getIdturma(), u.getNometurma(),u.getNomecurso(), u.getNomeusuario()});
                                 contador++;
                                 }
                                 lbtotal.setText(Integer.toString(contador));
}
    
