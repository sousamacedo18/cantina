    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
     JDialog.setDefaultLookAndFeelDecorated(true);
    int response = JOptionPane.showConfirmDialog(null, "Deseja Realmente Excluir Esse Registro?", "Confirm",
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
    if (response == JOptionPane.YES_OPTION) {       
           
    
        DefaultTableModel tabela = (DefaultTableModel) tblTurma.getModel();
                        tblTurma.setModel(tabela);
			int linhaSelecionada = -1;
			linhaSelecionada = tblTurma.getSelectedRow();
			if (linhaSelecionada >= 0) {
				int usu = (int) tblTurma.getValueAt(linhaSelecionada, 0);
				TurmaDao dao = new TurmaDao();
                                turmas.setIdturma(usu);
				dao.ExcluirTurma(turmas);
				tabela.removeRow(linhaSelecionada);
			} else {
				JOptionPane.showMessageDialog(null, "É necesário selecionar uma linha.");
			}
    }
    
           	
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
                Integer retorno=0;
                TurmaEditarForm turmaEditar = null;
        try {
                    try {
                        turmaEditar = new TurmaEditarForm(this,true);
                    } catch (IOException ex) {
                        Logger.getLogger(TurmaListaForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
        } catch (InterruptedException ex) {
            Logger.getLogger(TurmaListaForm.class.getName()).log(Level.SEVERE, null, ex);
        }
       		int linhaSelecionada = -1;
		linhaSelecionada = tblTurma.getSelectedRow();
                if (linhaSelecionada>=0){
                int id=(int)tblTurma.getValueAt(linhaSelecionada, 0);
                turmaEditar.editarCampos(id);
                turmaEditar.carregarCurso();
                turmaEditar.setVisible(true);
                retorno=turmaEditar.getRetorno();
                if (retorno==1){
                    preencherTabela();
                 }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Selecione um Curso");
                }      
               
    }//GEN-LAST:event_btnEditarActionPerformed

