    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        // TODO add your handling code here:

        int linhaSelecionada = -1;

        linhaSelecionada = tbTurma.getSelectedRow();
        if (linhaSelecionada>=0){
            int codigo=(int)tbTurma.getValueAt(linhaSelecionada, 0);
            String nome=(String)tbTurma.getValueAt(linhaSelecionada, 1);
            idturma=codigo;
            nometurma=nome;
            retorno=1;
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um Turma");
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

