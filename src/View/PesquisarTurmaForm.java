    public void carregarTudo(int idcurso, String nometurma){
            DefaultTableModel tabela = (DefaultTableModel) tbTurma.getModel();
            tbTurma.setModel(tabela);
            tbTurma.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            tbTurma.getColumnModel().getColumn(0).setPreferredWidth(50);
            tbTurma.getColumnModel().getColumn(1).setPreferredWidth(300);
            tabela.setNumRows(0);
            TurmaDao dao = new TurmaDao();

            for (TurmaController u : dao.CarregarTurmaIdCursoNomeCurso(idcurso, nometurma)) {
                tabela.addRow(new Object[]{u.getIdturma(), u.getNometurma()});

            }   
    }
    public void carregarTurma(String valor){
                if(valor.equals("")){
            JOptionPane.showMessageDialog(null, "Campos de pesquisa est√° vazio");
        }
        else{
            DefaultTableModel tabela = (DefaultTableModel) tbTurma.getModel();
            tbTurma.setModel(tabela);
            tbTurma.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            tbTurma.getColumnModel().getColumn(0).setPreferredWidth(60);
            tbTurma.getColumnModel().getColumn(1).setPreferredWidth(300);
            tbTurma.getColumnModel().getColumn(2).setPreferredWidth(220);
            tabela.setNumRows(0);
            TurmaDao dao = new TurmaDao();

            for (TurmaController u : dao.CarregarTurmaIdCursoNomeCurso(idcurso,valor)) {
                tabela.addRow(new Object[]{u.getIdturma(), u.getNometurma()});

            }
        }
    }
