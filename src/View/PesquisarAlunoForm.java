    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        if(txtCampo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campos de pesquisa est√° vazio");
            }
        else{
                DefaultTableModel tabela = (DefaultTableModel) tbAluno.getModel();
               tbAluno.setModel(tabela);
               tbAluno.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
               tbAluno.getColumnModel().getColumn(0).setPreferredWidth(60);
               tbAluno.getColumnModel().getColumn(1).setPreferredWidth(300);
               tbAluno.getColumnModel().getColumn(2).setPreferredWidth(220);
       		tabela.setNumRows(0);
		AlunoDao dao = new AlunoDao();

		for (AlunoController u : dao.CarregarPorParamentros(0, txtCampo.getText())) {
			tabela.addRow(new Object[]{u.getIdaluno(), u.getNomealuno(), u.getCpfaluno()});
		
                } 
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

